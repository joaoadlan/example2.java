public class CharDemo {
    public static void main(String[] args){
        char ch;
        ch = 'X';
        System.out.print("Ch contém " + ch );
        ch++; //incrementa o char
        System.out.print(" Ch é agora " + ch);
        ch = 90; //dá ao ch uma valor inteiro Z
        System.out.println(" Ch é agora " + ch);
    }
}
